'use strict';
var NodeCache = require( "node-cache" );

exports.register = function (server, options, next) {
    //人员信息缓存
    var personCache = new NodeCache();
    server.expose('person', personCache);

    return next();
};

exports.register.attributes = {
    name: 'cache'
};